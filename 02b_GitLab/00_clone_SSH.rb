# 1. Через интерфейс GitLab:
# a) Перейти на страницу проекта, который хнужно склонировать
# b) Найти кнопку "Clone", обычно она находится в верхней части страницы проекта, справа от названия проекта.
# с) Выберать способ клонирования, тк при нажатии на "Clone" появится несколько вариантов клонирования:
#   * Clone with HTTPS - простой и универсальный способ. Требует ввода логина и пароля (или токена личного доступа, рекомендуется сгенерировать и использовать емкнно его) при первом клонировании и при каждом push/pull, если не настроена аутентификация по протоколу HTTPS
#   * Clone with SSH   - безопасный и удобный способ, если настроен SSH-ключ. Не требует ввода логина и пароля
# d) Скопировать URL-адрес, соответствующий выбранному способу клонирования. Например: 
#   * `https://gitlab.com/username/project-name.git` (для HTTPS)
#   * `git@gitlab.com:username/project-name.git`     (для SSH)
# e) Перейти в директорию, куда нужно склонировать проект и выполнить команду `git clone`:
#   $ git clone https://gitlab.com/username/project-name.git
#   $ git clone git@gitlab.com:username/project-name.git



