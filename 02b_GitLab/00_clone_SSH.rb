# Добавление SSH-ключа в GitLab позволяет безопасно взаимодействовать с репозиториями, не вводя каждый раз пароль

# 1. Щелкните на значок своего профиля в правом верхнем углу.
# * Выберите "Settings / preferences" (Настройки)
# В боковой панели слева выберите "SSH Keys".**
# В поле "Key", вставьте скопированный публичный ключ.**
# Опционально: В поле "Title" введите название для этого ключа (например, "My Laptop").** Это поможет вам отличать ключи, если вы используете их на разных устройствах.
# Нажмите кнопку "Add key".**

# 2. Проверьте, что ключ работает (необязательно, но рекомендуется):**
# $ ssh -T git@gitlab.com
# Если в се настроено правильно, вы увидите сообщение, похожее на это: Welcome to GitLab, @your_username!


