# в) В боковой панели слева выбрать "SSH Keys"
# г) В поле "Key", вставить скопированный публичный ключ
# *) Опционально: В поле "Title" ввести название для этого ключа (например, "My Laptop"). Это поможет отличать ключи, которые используются на разных устройствах
# д) Нажать кнопку "Add key"

